
public class Aerithmatic {

	public static void main(String[] args) {
		

//Declare variable
		
		int num = 4;
		int num2 = 6;
//Addition
		int addition = num + num2;
		System.out.println("Addition Result " +addition);
		
//Substraction
		
	int substraction = num - num2;
	System.out.println(substraction);
	
//Multiplication
	
	int mul = num * num2;
	System.out.println(mul);
	
//Division
	
	int div = num / num2;
	System.out.println(div);
	
//Modulus
	
	int mod = num % num2;
	System.out.println(mod);
	
//Increment
	
	int a = 6;
	a = a + 1;
	System.out.println(a);
	
//Decrement
	
	int b = 6;
	b = b - 1;
	System.out.println(b);
	
//Another way to get result	
	b++;
	System.out.println(b);
	
	
	}

}
